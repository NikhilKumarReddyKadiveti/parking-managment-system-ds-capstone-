-- SQL Schema for Smart Parking Lot Management System (Supabase PostgreSQL)

-- 1. Create custom Users table (extends auth.users)
CREATE TABLE IF NOT EXISTS public.users (
    id UUID PRIMARY KEY REFERENCES auth.users(id) ON DELETE CASCADE,
    name TEXT NOT NULL,
    email TEXT NOT NULL UNIQUE,
    phone TEXT NOT NULL,
    role TEXT NOT NULL DEFAULT 'user', -- 'admin' or 'user'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

-- Enable Row Level Security
ALTER TABLE public.users ENABLE ROW LEVEL SECURITY;

-- Disable RLS constraints for ease of development/testing (optional, but using standard public read/write policy is best for a quick demo)
CREATE POLICY "Public Read/Write Access for users" ON public.users 
    FOR ALL USING (true) WITH CHECK (true);

-- 2. Create Vehicles table
CREATE TABLE IF NOT EXISTS public.vehicles (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    user_id UUID NOT NULL REFERENCES public.users(id) ON DELETE CASCADE,
    vehicle_number TEXT NOT NULL UNIQUE,
    vehicle_type TEXT NOT NULL DEFAULT 'Car', -- 'Car', 'Bike', 'Truck'
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

ALTER TABLE public.vehicles ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Public Read/Write Access for vehicles" ON public.vehicles 
    FOR ALL USING (true) WITH CHECK (true);

-- 3. Create Parking Slots table
CREATE TABLE IF NOT EXISTS public.parking_slots (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    slot_number TEXT NOT NULL UNIQUE,
    slot_type TEXT NOT NULL DEFAULT 'Four-Wheeler', -- 'Two-Wheeler', 'Four-Wheeler', 'Heavy-Vehicle'
    status TEXT NOT NULL DEFAULT 'available' -- 'available', 'occupied', 'booked'
);

ALTER TABLE public.parking_slots ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Public Read/Write Access for parking_slots" ON public.parking_slots 
    FOR ALL USING (true) WITH CHECK (true);

