-- 4. Create Parking Records table
CREATE TABLE IF NOT EXISTS public.parking_records (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vehicle_id BIGINT NOT NULL REFERENCES public.vehicles(id) ON DELETE CASCADE,
    slot_id BIGINT REFERENCES public.parking_slots(id) ON DELETE SET NULL,
    entry_time TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL,
    exit_time TIMESTAMP WITH TIME ZONE,
    total_hours NUMERIC(5,2),
    parking_fee NUMERIC(10,2),
    status TEXT NOT NULL DEFAULT 'active' -- 'active', 'completed', 'cancelled'
);

ALTER TABLE public.parking_records ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Public Read/Write Access for parking_records" ON public.parking_records 
    FOR ALL USING (true) WITH CHECK (true);

-- 5. Create Waiting Queue table
CREATE TABLE IF NOT EXISTS public.waiting_queue (
    id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
    vehicle_id BIGINT NOT NULL UNIQUE REFERENCES public.vehicles(id) ON DELETE CASCADE,
    queue_position INT NOT NULL,
    created_at TIMESTAMP WITH TIME ZONE DEFAULT timezone('utc'::text, now()) NOT NULL
);

ALTER TABLE public.waiting_queue ENABLE ROW LEVEL SECURITY;
CREATE POLICY "Public Read/Write Access for waiting_queue" ON public.waiting_queue 
    FOR ALL USING (true) WITH CHECK (true);
